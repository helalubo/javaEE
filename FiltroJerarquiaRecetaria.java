package pcup.domain.model;

public class FiltroJerarquiaRecetaria {
	private String cod;
	private String des;
	public FiltroJerarquiaRecetaria() {
		
	}
	public void setCod(String cod) {
		this.cod = cod;
	}
	public String getCod() {
		return cod;
	}
	public void setDes(String des) {
		this.des = des;
	}
	public String getDes() {
		return des;
	}
}